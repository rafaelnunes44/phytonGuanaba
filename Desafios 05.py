"""
05. Faça um programa que leia um numero inteiro e monstre na tela seu sucessor e seu antecessor

"""


n1 = int(input('Digite um número, que monstraremos seu sucessor e seu antecessor: '))
su = n1 + 1
an = n1 - 1
print('O sucessor de {} é {} e seu antecessor é {}'.format(n1, su , an))
















